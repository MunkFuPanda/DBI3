use president;

drop table if exists Election;
drop table if exists Pres_Marriage;
drop table if exists Admin_Pr_Vp;
drop table if exists Administration;
drop table if exists Pres_Hobby;
drop table if exists President;
drop table if exists State;

create table State
(
	State_Name varchar(17) primary key,
	Admin_Entered int,
	Year_Entered int not null
);

create table President
(
	Pres_Name varchar(17) primary key,
	Birth_Year int not null,
	Years_Serv int not null,
	Death_Age int,
	Party varchar(12) not null,
	State_Born varchar(17) not null references State
);

create table Pres_Hobby
(
	Pres_Name varchar(17) references President,
	Hobby varchar(18),
	primary key (Pres_Name, Hobby)
);

create table Administration
(
	Admin_Nr int,
	Pres_Name varchar(17) references President,
	Year_Inaugurated int not null,
	primary key (Admin_Nr, Pres_Name )
);

create table Admin_Pr_Vp
(
	Admin_Nr int,
	Pres_Name varchar(17),
	Vice_Pres_Name varchar(17),
	foreign key ( Admin_Nr, Pres_Name ) references Administration,
	primary key ( Admin_Nr, Pres_Name, Vice_Pres_Name ),
	check (Pres_Name != Vice_Pres_Name)
);

create table Pres_Marriage
(
	Pres_Name varchar(17) references President,
	Spouse_Name varchar(17),
	Pr_Age int not null,
	Sp_Age int not null,
	Nr_Children int not null,
	Mar_Year int not null,
	primary key(Pres_Name, Spouse_Name)
);

create table Election
(
	Election_Year int,
	Candidate varchar(17),
	Votes int,
	Winner_Loser_Indic char(1) not null,
	primary key(Election_Year, Candidate )
);

